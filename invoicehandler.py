class InvoiceHandler:
 def handle_invoice(self, invoice):
   print("Total:", invoice['total'])

class InvoicePersistence:
 def __init__(self, db_name="mi_base_de_datos"):
    self.db_name = db_name

 def save_to_db(self, invoice):
   print(f"Guardando factura de {invoice['cliente']} en {self.db_name}…")

mi_factura = {"total": 100, "cliente": "Juan Pérez", "items": ["producto1", "producto2"]}
